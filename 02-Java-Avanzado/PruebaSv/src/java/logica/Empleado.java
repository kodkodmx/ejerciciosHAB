package logica;


public class Empleado {
    
    String nombre;
    String apellido;
    String domicilio;
    String telefono;

    public Empleado(String nombre, String apellido, String domicilio, String telefono) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.domicilio = domicilio;
        this.telefono = telefono;
    }
    
    
    
}
